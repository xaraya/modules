/*
 * Get the question and answer details for a user survey
 * and group.
 * This function just fetches the data for the questions,
 * and for the current responses. The data can be passed
 * to the objects for rendering later.
 */

function surveys_userapi_getusergroupquestions($args) {

    $questions = xarModAPIfunc('surveys', 'user', 'getusergroupsquestions', $args);

    if (empty($questions) || !is_array($questions) || count($questions) <> 1) {
        // Expecting exactly one element in the array.
        return;
    }

    // Return the first element in the array.
    return reset($questions);
}

